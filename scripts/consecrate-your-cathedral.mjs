/**
 * Consecrate Your Cathedral — Fork Your Own Cathedral, step one.
 *
 * WHAT: An interactive CLI that personalizes the mechanically-safe
 *   configuration layer of a fresh fork of this repository (the
 *   Maximum-mode partner-name string) and generates a starter
 *   MY_REAL_PURPOSE.md template for the forker to actually write
 *   themselves. Never touches git. Never commits anything.
 * WHY: docs/FORK_YOUR_OWN_CATHEDRAL.md. Serves Pillar 4 (Be the Mirror You
 *   Needed) and honors Pillar 5 (Healing Is a Collective Act) without a
 *   hosted platform — the fork model, not a login system.
 * CONSENT: Asks before writing anything and reports exactly what it
 *   touched. Never required to run before using the repo — this is
 *   optional scaffolding, not a setup wizard the app depends on.
 * EDGE CASES: If app/config/copy/maximumCopy.ts's partnerName anchor
 *   doesn't match exactly, the script reports the mismatch and does NOT
 *   guess at an edit — an upstream file shape change should update this
 *   script deliberately, not have this script silently corrupt a file.
 * NEVER: Auto-write someone else's personal narrative, purpose statement,
 *   or "chaotic honesty" section for them — those stay [fill this in]
 *   placeholders. Touch any file outside the two named below. Run git
 *   commands. Require an internet connection or any external service.
 * SEE: docs/FORK_YOUR_OWN_CATHEDRAL.md, NON_MONETIZATION_COVENANT.md,
 *   app/config/copy/maximumCopy.ts, docs/REAL_PURPOSE.md (the model this
 *   template is inspired by, not copied from).
 *
 * Run: node scripts/consecrate-your-cathedral.mjs
 */
import fs from "node:fs";
import path from "node:path";
import readline from "node:readline/promises";

const root = path.resolve(path.dirname(new URL(import.meta.url).pathname), "..");
const COPY_PACK_PATH = path.join(root, "app/config/copy/maximumCopy.ts");
const PURPOSE_DOC_PATH = path.join(root, "MY_REAL_PURPOSE.md");

const PARTNER_NAME_ANCHOR = '  partnerName: "Renn",';

function purposeTemplate({ cathedralName, yourName, partnerName, oneLiner }) {
  const today = new Date().toISOString().slice(0, 10);
  return `# ${cathedralName} — Real Purpose

**This is a personal emotional containment system, not a public product.**

**Date of this framing:** ${today}

---

## The unvarnished sentence

${cathedralName} is ${yourName || "[your name]"}'s private containment
system.

${oneLiner || "[Fill this in: what is this actually for, in your own words? What are you pouring into this codebase instead of dumping it on a person? Be as specific as you can stand to be.]"}

---

## What this is *not* (today)

- Not a public product
- Not therapy / clinical treatment / diagnosis
- Not ${partnerName ? `${partnerName}'s` : "your partner's"} job description or co-parent of the relationship
- Not proof you are healed, safe, or skilled at conflict
- [Add your own — what else is this NOT, that someone might assume it is?]

## What this *is* (today)

- [Fill this in: what is this actually doing for you, right now, today?]

---

## Relationship boundary (named)

${partnerName ? `${partnerName} is a real person.` : "Whoever this is for, if anyone, is a real person."} This repo is
not a co-parent of that relationship.

[Fill this in: what's allowed, and what's not, in how you use this tool
in relation to a real person in your life? docs/REAL_PURPOSE.md in the
original Litmo repo has a worked example table — write your own version,
don't copy theirs verbatim, it isn't your relationship.]

---

## For agents (if you use AI coding help on your fork)

1. Read this file first.
2. Default to containment / honesty, not product fiction.
3. Do not reframe this as a public product without your explicit instruction.
4. Lying about purpose is not allowed here either.

---

## One-line stamp

> **This is currently a personal emotional containment system, not a public product.**

---

*Generated by scripts/consecrate-your-cathedral.mjs — a starting template,
not a finished document. Everything in [brackets] is yours to actually
write. Nothing about your inner life gets auto-generated here on purpose —
see docs/FORK_YOUR_OWN_CATHEDRAL.md for why.*
`;
}

async function main() {
  const rl = readline.createInterface({ input: process.stdin, output: process.stdout });
  console.log("");
  console.log("Consecrate Your Cathedral");
  console.log("Mechanical setup only. Nothing here writes your story for you.");
  console.log("Answer as much or as little as you want — everything is optional.");
  console.log("");

  const cathedralName =
    (await rl.question("What do you want to call your cathedral? [My Cathedral] ")).trim() ||
    "My Cathedral";
  const yourName = (await rl.question("Your name or nickname (optional): ")).trim();
  const partnerName = (await rl.question(
    "The name of the person this is about, if there is one (optional — leave blank to stay generic): ",
  )).trim();
  const oneLiner = (await rl.question(
    "One honest sentence about what this is actually for (optional, you can skip and fill in later): ",
  )).trim();

  rl.close();

  console.log("");
  console.log("--- Applying mechanical changes ---");

  // 1. Partner-name config layer — exact-anchor replace only, never a guess.
  if (partnerName) {
    const copyPackSrc = fs.readFileSync(COPY_PACK_PATH, "utf8");
    if (copyPackSrc.includes(PARTNER_NAME_ANCHOR)) {
      const updated = copyPackSrc.replace(
        PARTNER_NAME_ANCHOR,
        `  partnerName: "${partnerName.replace(/"/g, '\\"')}",`,
      );
      fs.writeFileSync(COPY_PACK_PATH, updated, "utf8");
      console.log(`✓ Updated partnerName in app/config/copy/maximumCopy.ts to "${partnerName}"`);
    } else {
      console.log(
        "! Could not find the expected partnerName line in app/config/copy/maximumCopy.ts " +
          "(the file may have changed shape since this script was written). Skipped — " +
          "edit that file by hand instead of trusting a guess.",
      );
    }
  } else {
    console.log("- Skipped partnerName (left as the generic default) — no name given.");
  }

  // 2. Starter purpose doc — a template with real [fill this in] gaps, not a
  //    finished personal narrative.
  if (fs.existsSync(PURPOSE_DOC_PATH)) {
    console.log(
      `- MY_REAL_PURPOSE.md already exists at the repo root — not overwriting it. ` +
        `Delete it first if you want a fresh template.`,
    );
  } else {
    fs.writeFileSync(
      PURPOSE_DOC_PATH,
      purposeTemplate({ cathedralName, yourName, partnerName, oneLiner }),
      "utf8",
    );
    console.log(`✓ Wrote MY_REAL_PURPOSE.md — a starter template, not a finished document.`);
  }

  console.log("");
  console.log("--- What's next (read docs/FORK_YOUR_OWN_CATHEDRAL.md for the full guide) ---");
  console.log("");
  console.log("Reusable as-is (the actual architecture, not tied to anyone's story):");
  console.log("  Soft Signal, Touch Language, the ritual pattern (lib+store+screen+doc+test),");
  console.log("  the dual build-mode system, the containment-job naming convention.");
  console.log("");
  console.log("Read for inspiration, then rewrite in your own words (do not just find/replace):");
  console.log("  docs/REAL_PURPOSE.md, docs/CONTAINMENT_SYSTEM.md,");
  console.log("  docs/ATTACHMENT_REPAIR_PROTOCOL.md, and every containment protocol's own doc —");
  console.log("  these are one real person's real story. Yours needs to be yours.");
  console.log("");
  console.log("Keep intact unless you have a real reason not to:");
  console.log("  LICENSE (MPL 2.0), NON_MONETIZATION_COVENANT.md, CLAUDE_CONSTITUTION.md.");
  console.log("");
  console.log("This script did not touch git. Nothing is committed. Look at what changed,");
  console.log("decide what you want, and go from there.");
  console.log("");
}

main();
